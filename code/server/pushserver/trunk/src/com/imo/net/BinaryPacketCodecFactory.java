package com.imo.net;

import org.apache.mina.core.session.IoSession;
import org.apache.mina.filter.codec.ProtocolCodecFactory;
import org.apache.mina.filter.codec.ProtocolDecoder;
import org.apache.mina.filter.codec.ProtocolEncoder;

public class BinaryPacketCodecFactory implements ProtocolCodecFactory {
    private BinaryPacketDecoder decoder;
    private BinaryPacketEncoder encoder;

    public BinaryPacketCodecFactory() {
        decoder = new BinaryPacketDecoder();
        encoder = new BinaryPacketEncoder();
    }

    @Override
    public ProtocolEncoder getEncoder(IoSession ioSession) throws Exception {
        return encoder;
    }

    @Override
    public ProtocolDecoder getDecoder(IoSession ioSession) throws Exception {
        return decoder;
    }
} // class BinaryPacketCodecFactory end
